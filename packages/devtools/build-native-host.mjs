#!/usr/bin/env node

/**
 * Build the native-host runtime into dist/service-host and dist/render-host.
 *
 * Two kinds of inputs:
 *   1. TypeScript (`src/service-host/*.ts` — sync-api-patch + sync-impls) is
 *      compiled to ESM `.js` via tsconfig.service-host.json (preserving the
 *      `./x.js` import specifiers that service.html / the preload rely on).
 *   2. Hand-written CommonJS preloads (`*.cjs`) and HTML are copied verbatim —
 *      they are runtime-loaded by absolute path and must not be transpiled.
 *
 * Previously these outputs were produced ad-hoc and only existed in a stale
 * local dist (not regenerated by `pnpm build`), so a clean checkout / CI had no
 * service-host bundle. This step closes that gap; it is chained into `build`.
 *
 * BUILD-ORDER INDEPENDENCE (step 1a below): the service-host tsc
 * (tsconfig.service-host.json, rootDir=src) follows the sync-impls' import of
 * the SHARED `src/simulator/device-shell/menu-button-geometry.ts` and emits it
 * to `dist/simulator/device-shell/menu-button-geometry.js` — a dir `build:simulator`
 * (vite, emptyOutDir) WIPES. A `build:simulator` running after this step (or a
 * partial rebuild in the wrong order) would delete that file, 404-ing the entire
 * sync-api-patch ES-module load (→ wx.getSystemInfoSync()/{set,get}StorageSync
 * silently fall back to upstream's boot-cached versions). Step 1a copies the
 * geometry module into the service-host dir (vite never touches it) and rewrites
 * the import, so build order no longer matters.
 */

import { spawnSync } from 'node:child_process'
import { cpSync, existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, '../..')
const DIMINA_FE_PACKAGES = join(ROOT, 'dimina/fe/packages')
const NATIVE_HOST_ASSETS = [
  { src: join(DIMINA_FE_PACKAGES, 'service/dist'), dest: join(__dirname, 'dist/native-host/service') },
  { src: join(DIMINA_FE_PACKAGES, 'render/dist'), dest: join(__dirname, 'dist/native-host/render') },
  { src: join(DIMINA_FE_PACKAGES, 'common/dist'), dest: join(__dirname, 'dist/native-host/common') },
]

// The render layer's base stylesheet (rpx→rem `:root` font-size + reset + .dd-*
// component defaults) ships only with the container package, not render/. The
// render-host pageFrame.html links it; vendor the single self-contained file so
// the dist never reaches into the submodule at runtime (mirrors the dirs above).
const NATIVE_HOST_FILES = [
  {
    src: join(DIMINA_FE_PACKAGES, 'container/dist/assets/pageFrame.css'),
    dest: join(__dirname, 'dist/native-host/container/pageFrame.css'),
  },
]

// 1. Compile the service-host TypeScript.
const tsc = spawnSync('npx', ['tsc', '-p', 'tsconfig.service-host.json'], {
  cwd: __dirname,
  stdio: 'inherit',
  shell: true,
})
if (tsc.status !== 0) process.exit(tsc.status ?? 1)

// 1a. Sever the service-host bundle's runtime dependency on `dist/simulator`.
//
// The service-host tsc (rootDir=src) follows `sync-impls/menu-button.ts`'s import
// of the SHARED `src/simulator/device-shell/menu-button-geometry.ts` and emits it
// to `dist/simulator/device-shell/menu-button-geometry.js`. But `build:simulator`
// (vite, emptyOutDir) WIPES all of `dist/simulator`, so a `build:simulator` that
// runs AFTER this step (or a partial rebuild in the wrong order) deletes that
// file — silently breaking the ENTIRE `sync-api-patch.js` ES-module load (its
// transitive import 404s), which leaves `wx.{set,get}StorageSync` /
// `getSystemInfoSync` as upstream's boot-cached versions (storage stops round-
// tripping; live device sizing reverts to the boot device).
//
// Copy the (just-emitted, self-contained) geometry module into the service-host
// dir — which vite never touches — and rewrite the emitted import to resolve
// there. Now `build:native-host` is self-contained: build order no longer
// matters and `build:simulator` can never delete a file the service host needs.
{
  const geomSrc = join(__dirname, 'dist/simulator/device-shell/menu-button-geometry.js')
  const geomDest = join(__dirname, 'dist/service-host/sync-impls/menu-button-geometry.js')
  if (!existsSync(geomSrc)) {
    console.error('[build-native-host] expected', geomSrc, 'after tsc — service-host compile is broken')
    process.exit(1)
  }
  cpSync(geomSrc, geomDest)
  const menuButtonJs = join(__dirname, 'dist/service-host/sync-impls/menu-button.js')
  const patched = readFileSync(menuButtonJs, 'utf8')
    .replaceAll('../../simulator/device-shell/menu-button-geometry.js', './menu-button-geometry.js')
  writeFileSync(menuButtonJs, patched)
}

// 1b. Bundle the render-guest inspector into a self-contained IIFE. Main injects
// this into each render-host <webview>'s main world via executeJavaScript (see
// render-inspect.ts); it has no electron/node imports, so it bundles for the
// browser. Reuses wxml-extract through the bundle — no logic duplication.
const inspect = spawnSync(
  'npx',
  [
    'esbuild',
    'src/render-host/render-inspect.ts',
    '--bundle',
    '--format=iife',
    '--platform=browser',
    '--target=chrome120',
    '--outfile=dist/render-host/render-inspect.js',
  ],
  { cwd: __dirname, stdio: 'inherit', shell: true },
)
if (inspect.status !== 0) process.exit(inspect.status ?? 1)

// 2. Copy upstream native-host dependencies into package-local dist assets.
for (const { src, dest } of NATIVE_HOST_ASSETS) {
  mkdirSync(dirname(dest), { recursive: true })
  cpSync(src, dest, { recursive: true })
}
for (const { src, dest } of NATIVE_HOST_FILES) {
  mkdirSync(dirname(dest), { recursive: true })
  cpSync(src, dest)
}

// 3. Copy the static (non-TS) assets, rewriting HTML upstream refs to the
// package-local native-host assets above.
const ASSETS = [
  'src/service-host/preload.cjs',
  // Pure helper required by preload.cjs (relative `./sourcemap-rewrite.cjs`);
  // copied verbatim so the runtime require resolves from dist/service-host.
  'src/service-host/sourcemap-rewrite.cjs',
  'src/service-host/service.html',
  'src/render-host/preload.cjs',
  'src/render-host/pageFrame.html',
]
for (const rel of ASSETS) {
  const dest = join(__dirname, rel.replace(/^src\//, 'dist/'))
  mkdirSync(dirname(dest), { recursive: true })
  const src = join(__dirname, rel)
  if (rel === 'src/service-host/service.html') {
    const html = readFileSync(src, 'utf8')
      .replaceAll('../../../../dimina/fe/packages/service/dist/', '../native-host/service/')
    writeFileSync(dest, html)
  } else if (rel === 'src/render-host/pageFrame.html') {
    const html = readFileSync(src, 'utf8')
      .replaceAll('../../../../dimina/fe/packages/common/dist/', '../native-host/common/')
      .replaceAll('../../../../dimina/fe/packages/render/dist/', '../native-host/render/')
      .replaceAll('../../../../dimina/fe/packages/container/dist/assets/', '../native-host/container/')
    writeFileSync(dest, html)
  } else {
    cpSync(src, dest)
  }
}

console.log('native-host runtime built → dist/service-host, dist/render-host')
