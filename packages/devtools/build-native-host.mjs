#!/usr/bin/env node

/**
 * Build the native-host runtime into dist/service-host and dist/render-host.
 *
 * Two kinds of inputs:
 *   1. TypeScript (`src/service-host/*.ts` — sync-api-patch + sync-impls) is
 *      compiled to ESM `.js` via tsconfig.service-host.json (preserving the
 *      `./x.js` import specifiers that service.html / the preload rely on).
 *   2. Hand-written CommonJS preloads (`*.cjs`) and HTML are copied verbatim —
 *      they are runtime-loaded by absolute path and must not be transpiled.
 *
 * Previously these outputs were produced ad-hoc and only existed in a stale
 * local dist (not regenerated by `pnpm build`), so a clean checkout / CI had no
 * service-host bundle. This step closes that gap; it is chained into `build`.
 */

import { spawnSync } from 'node:child_process'
import { cpSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, '../..')
const DIMINA_FE_PACKAGES = join(ROOT, 'dimina/fe/packages')
const NATIVE_HOST_ASSETS = [
  { src: join(DIMINA_FE_PACKAGES, 'service/dist'), dest: join(__dirname, 'dist/native-host/service') },
  { src: join(DIMINA_FE_PACKAGES, 'render/dist'), dest: join(__dirname, 'dist/native-host/render') },
  { src: join(DIMINA_FE_PACKAGES, 'common/dist'), dest: join(__dirname, 'dist/native-host/common') },
]

// 1. Compile the service-host TypeScript.
const tsc = spawnSync('npx', ['tsc', '-p', 'tsconfig.service-host.json'], {
  cwd: __dirname,
  stdio: 'inherit',
  shell: true,
})
if (tsc.status !== 0) process.exit(tsc.status ?? 1)

// 1b. Bundle the render-guest inspector into a self-contained IIFE. Main injects
// this into each render-host <webview>'s main world via executeJavaScript (see
// render-inspect.ts); it has no electron/node imports, so it bundles for the
// browser. Reuses wxml-extract through the bundle — no logic duplication.
const inspect = spawnSync(
  'npx',
  [
    'esbuild',
    'src/render-host/render-inspect.ts',
    '--bundle',
    '--format=iife',
    '--platform=browser',
    '--target=chrome120',
    '--outfile=dist/render-host/render-inspect.js',
  ],
  { cwd: __dirname, stdio: 'inherit', shell: true },
)
if (inspect.status !== 0) process.exit(inspect.status ?? 1)

// 2. Copy upstream native-host dependencies into package-local dist assets.
for (const { src, dest } of NATIVE_HOST_ASSETS) {
  mkdirSync(dirname(dest), { recursive: true })
  cpSync(src, dest, { recursive: true })
}

// 3. Copy the static (non-TS) assets, rewriting HTML upstream refs to the
// package-local native-host assets above.
const ASSETS = [
  'src/service-host/preload.cjs',
  'src/service-host/service.html',
  'src/render-host/preload.cjs',
  'src/render-host/pageFrame.html',
]
for (const rel of ASSETS) {
  const dest = join(__dirname, rel.replace(/^src\//, 'dist/'))
  mkdirSync(dirname(dest), { recursive: true })
  const src = join(__dirname, rel)
  if (rel === 'src/service-host/service.html') {
    const html = readFileSync(src, 'utf8')
      .replaceAll('../../../../dimina/fe/packages/service/dist/', '../native-host/service/')
    writeFileSync(dest, html)
  } else if (rel === 'src/render-host/pageFrame.html') {
    const html = readFileSync(src, 'utf8')
      .replaceAll('../../../../dimina/fe/packages/common/dist/', '../native-host/common/')
      .replaceAll('../../../../dimina/fe/packages/render/dist/', '../native-host/render/')
    writeFileSync(dest, html)
  } else {
    cpSync(src, dest)
  }
}

console.log('native-host runtime built → dist/service-host, dist/render-host')
