/**
 * E2E tests for the dimina-devtools simulator TabBar feature.
 *
 * Strategy:
 *   - Self-launch one Electron instance in `auto` mode (so the WebSocket
 *     automation server boots and we can drive the simulator via the real
 *     miniprogram-automator npm package).
 *   - Open the source fixture mini-app at `e2e/fixtures/tabbar-app` (3 tabs:
 *     Home, Cart, Me + 1 non-tab detail page).
 *   - All tests share one Electron + one open project (`serial` mode),
 *     because devtools compile takes 3–8s and the tests don't mutate the
 *     filesystem.
 *
 * Discovery convention:
 *   - We never hard-code internal selectors invented from training data.
 *   - The first test (`tabbar.renders`) prints the live tab-bar DOM so the
 *     subsequent tests can rely on what is actually there. If selectors
 *     change in the future, swap the constants near the top.
 */

