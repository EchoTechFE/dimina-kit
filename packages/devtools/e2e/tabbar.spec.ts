  // ── 11. setTabBarStyle ─────────────────────────────────────────────
  //
  // The .dimina-tabbar-text element has a `transition: color 0.2s ease` CSS
  // rule, so getComputedStyle returns the *interpolated* colour during the
  // transition window. textColor is therefore asserted via polled expects so
  // the transition can settle before the equality check.
  test('wx.setTabBarStyle changes background/text/selected colours', async () => {
    await miniProgram.callWxMethod('setTabBarStyle', {
      color: '#ff0000',
      selectedColor: '#00ff00',
      backgroundColor: '#000000',
    })
    await expect
      .poll(async () => (await readTabItemByLabel('Cart'))?.bgColor ?? '', { timeout: 5000 })
      .toBe('rgb(0, 0, 0)')
    // Cart (unselected) should be red — poll past the color transition.
    await expect
      .poll(async () => (await readTabItemByLabel('Cart'))?.textColor ?? '', { timeout: 5000, intervals: [50, 100, 200] })
      .toBe('rgb(255, 0, 0)')
    // Home (selected — current tab) should be green.
    await expect
      .poll(async () => (await readTabItemByLabel('Home'))?.textColor ?? '', { timeout: 5000, intervals: [50, 100, 200] })
      .toBe('rgb(0, 255, 0)')
  })

